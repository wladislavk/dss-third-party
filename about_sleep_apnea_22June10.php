          <tr>
            <td valign="top" class="page3_bg2"><img src="images/image_1.gif" width="160" height="131" hspace="5" vspace="5" border="1" align="left" />Apnea literally means "cessation of breath".Obstructive Sleep Apnea is simply what the words state - obstructions that happen during sleep that cause a cessation of breath.  The human upper airway is encircled by muscles.  The largest of these muscles is the tongue.  When we are awake we have tonicity or tightness in these muscles of the upper airway but during sleep these muscles relax. <br />
                <br />
              As we breathe during sleep, these relaxed muscles may vibrate causing snoring and can eventually collapse into the airway causing a decrease of airflow into the lungs.  Usually, due to this suffocation, our brain eventually wakes up out of the deeper stages of sleep and tightens up the muscles of the upper airway to again allow the airflow to return to normal. This can unknowingly happen hundreds of times a night resulting in excessive daytime sleepiness, mental impairment, cardiovascular problems and a multitude of other undesirable side effects. </td>

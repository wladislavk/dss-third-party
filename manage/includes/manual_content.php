<h1><a name="_Toc350778743">OVERVIEW</a></h1>

<p class=MsoNormal style='text-align:justify'>&nbsp;</p>

<p class=MsoNormal style='text-align:justify'>Whether you are a neophyte to
dental sleep medicine or a seasoned professional, we hope that you will find
this procedures manual helpful and will refer back to it often.  We have made
every attempt to make the process of screening, testing, and treating sleep
disordered breathing patients as simple as possible.   In spite of the millions
of undiagnosed sleep apneics out there, dentists have failed to “get it” and
been slow to adopt any attitude of screening or treatment.  Congratulations! 
You have made a commitment and clear choice to move forward and be a part of
helping people sleep better, feel better, and live longer.</p>

<p class=MsoNormal style='text-align:justify'>&nbsp;</p>

<p class=MsoNormal style='text-align:justify'>Patients are walking through your
office doors every day that have this deadly disease and do not know it.  You
have been screening for oral cancer for years, because it is the right thing to
do.  You now embark on a new quest: to find the sleep apneics in your
practice.  Screening tablets and software make this a simple task and all you
have to do is commit yourself and your staff to doing it!   Diagnosis of
Obstructive Sleep Apnea (OSA) is established by a physician (MD) via some form
of sleep test, either a polysomnogram (PSG) or home sleep test (HST).  The
historical medical model has pushed for the PSG, or in-lab sleep test, followed
by treatment with Continuous Positive Airway Pressure (CPAP).  As a result, in
nearly thirty years, fewer than 10% of the patients who have this deadly
disease have been diagnosed and even fewer still accept CPAP as a viable
treatment.  In short, our system has failed.  People don’t want to go to a lab
to get a test; when they realize they have the disease, they don’t want to use
CPAP.  Good for us as dentists!  We now have a much easier, more patient
friendly way both to diagnose and treat patients:  Home Sleep Tests and
dentists providing Mandibular Repositioning Devices (MRD).   </p>

<p class=MsoNormal style='text-align:justify'>&nbsp;</p>

<p class=MsoNormal style='text-align:justify'>Diagnosed patients are likely to
get insurance coverage for your treatment (dental device therapy), whereas
undiagnosed patients are not.  Assume that your patient will want to know what
insurance will cover in order to proceed with treatment, so do not
underestimate the importance of insurance in your treatment regimen.  Next you
will want to consult with your patient to review the results of the patient’s
sleep test and to determine whether or not he is a candidate for dental device
therapy.  Assuming he is, you will decide on a treatment plan, choose the
appropriate dental device, and secure financial arrangements.  Impressions are
taken and a clinical exam is performed.  Lab prescriptions are filled out and
the patient is scheduled for device delivery.  You will fit the dental device
and establish a titration <i>(adjustment)</i> protocol and schedule the patient
for follow up every 3-4 weeks, following the <img width=284 height=205
src="includes/manual_assets/image001.gif"
align=left hspace=12
alt="http://www.zoedental.com/assets/Image/sleep-apnea/sleepapnea_TAP-3.png">patient
closely, managing their titration, their side effects, and adjusting their
device based on subjective findings.   </p>

<p class=MsoNormal style='text-align:justify'>&nbsp;</p>

<p class=MsoNormal style='text-align:justify'>When you believe the device is
working satisfactorily, you will schedule the patient for a follow up home
sleep test and then review this with the patient.  It is a good idea to involve
the patient’s Primary Care Physician (PCP) and we recommend you do this by
communicating through progress notes and letters.  The PCP will help determine
device therapy efficacy.  You may need to continue titration of your dental
device based on this HST, or you may place the patient on annual recall, having
established an end point in your therapy. </p>


<p class=MsoNormal style='margin-left:.75in;text-align:justify;text-indent:
-.25in'><span style='font-family:Symbol'>·<span style='font:7.0pt "Times New Roman"'>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;

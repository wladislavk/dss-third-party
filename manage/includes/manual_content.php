<h1><a name="_Toc350778788"></a><a name="_Toc298865481"></a><a
name="_INSURANCE_INFORMATION"></a>INSURANCE INFORMATION</h1>

<p class=MsoNormal style='text-align:justify'>&nbsp;</p>

<p class=MsoNormal style='text-align:justify'>Specific information from the
patient and related sleep labs and doctors is needed in order to verify,
pre-authorize and file claims in an efficient and timely manner.  Most of this
information will be gathered in the initial telephone call or through the
patient’s utilization of the web portal.</p>

<p class=MsoNormal style='text-align:justify'> </p>

<p class=MsoNormal style='text-align:justify'>The information in this section
is designed to give a brief overview on medical insurance in general.</p>

<p class=MsoNormal style='text-align:justify'>&nbsp;</p>

<p class=MsoNormal style='text-align:justify'>There are two categories of insurance
entities we will deal with:  </p>

<p class=MsoNormal style='text-align:justify'>&nbsp;</p>

<p class=MsoNormal style='text-align:justify'>            The U.S. Government
(Medicare) and</p>

<p class=MsoNormal style='text-align:justify'>            Private Insurance
Companies</p>

<p class=MsoNormal style='text-align:justify'>&nbsp;</p>

<p class=MsoNormal style='text-align:justify'>Regarding  Medicare, we will need
to determine whether or not you want to:</p>

<p class=MsoNormal style='text-align:justify'>            Be a Participating
Provider (PP), </p>

<p class=MsoNormal style='text-align:justify'>            Be a Non-Participating
Provider (NPP), or</p>

<p class=MsoNormal style='text-align:justify'>            Opt out of Medicare
entirely. </p>

<p class=MsoNormal style='text-align:justify'>&nbsp;</p>

<p class=MsoNormal style='text-align:justify'>In addition, if you elect to
treat Medicare patients as a PP or NPP, you will need to apply for and obtain a
Durable Medical Equipment Supplier number.  Other insurance companies will deal
with you as a provider (PPO) or non-provider for their plan.  Unless you have
signed a contract with them, you will be a non-participating provider (NPP). 
Becoming a provider for any insurance company is a laborious process, requires
a lengthy contract for you to review, and also involves negotiating a fee.  The
advantages of being a PP for an insurance company include more patient volume
(presumed) and a streamlined process of reimbursement.  The main disadvantages
are a lower fee for your service and the possibility that they will not
reimburse you for a service that another dentist has been contracted to
provide.</p>

<p class=MsoNormal style='text-align:justify'>&nbsp;</p>

<p class=MsoNormal style='text-align:justify'>Remember that preauthorization of
the medical benefits is not a guarantee of payment. The patient is ultimately
responsible for his or her own account. </p>


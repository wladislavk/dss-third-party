<p class=MsoNormal style='text-align:justify'>You know the first answer.  In
order to answer the insurance question, on the day the patient is sitting in
your office for a consultation, you must have <b><i>previously</i></b> obtained
information about their specific insurance policy and benefits AND engaged
someone to find out.  </p>


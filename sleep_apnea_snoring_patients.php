      <p>Obstructive  Sleep Apnea Syndrome (OSA) is a life threatening disorder affecting over 18  million Americans. Research estimates that up to 9% of adult males and 4% of  females suffer from sleep disordered breathing, yet fewer than 10% have been  diagnosed.In fact, the most recent studies have shown that 1 in 4 adults in the  United States (31% of all  men and 21% of all women over 18) are at &quot;high risk&quot; for OSA (based on analysis of the National Sleep  Foundation's 2005 Sleep in America  survey). Furthermore 40% of Americans snore and 40% of snorers  have OSA with no signs or symptoms of the disease</p></td>

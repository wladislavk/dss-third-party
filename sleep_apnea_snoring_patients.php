      <ol start="1" type="1">
        <li>Snoring</li>
        <li>Waking up due to gasping or choking</li>
        <li>Excessive daytime sleepiness</li>
        <li>Irritability</li>
        <li>Memory loss</li>
        <li>Nighttime grinding of teeth</li>
        <li>Restless or unrefreshed sleep</li>
        <li>Frequent waking during sleep</li>
      </ol>

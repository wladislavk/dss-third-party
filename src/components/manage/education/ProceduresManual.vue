                be doing fantastic and think you are the greatest, or he may be having doubts
                about why he ever did this in the first place. The happy patient requires
                little encouragement, but the struggling patient requires more attention and
                coddling. Almost ALL patients can learn to tolerate a dental device,
                especially with you cheering them on.</p>


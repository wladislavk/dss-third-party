            <p class="MsoNormal">&nbsp;</p>

            <p class="MsoNormal" style="text-align:justify"><a name="_Toc350778766"><span class="Heading3Char">Goals of First Follow-Up Appointment</span></a>:</p>

            <p class="MsoListParagraphCxSpFirst" style="margin-left:1.0in;text-align:justify;
text-indent:-.25in"><span style="font-family:Symbol">•<span style="font:7.0pt &quot;Times New Roman&quot;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
            </span></span>Address Side Effects</p>

            <p class="MsoListParagraphCxSpMiddle" style="margin-left:1.0in;text-align:justify;
text-indent:-.25in"><span style="font-family:Symbol">•<span style="font:7.0pt &quot;Times New Roman&quot;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
            </span></span>Encourage the patient</p>

            <p class="MsoListParagraphCxSpLast" style="margin-left:1.0in;text-align:justify;
text-indent:-.25in"><span style="font-family:Symbol">•<span style="font:7.0pt &quot;Times New Roman&quot;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
            </span></span>Assess subjective improvements</p>

            <p class="MsoNormal" style="text-align:justify">&nbsp;</p>

            <p class="MsoNormal" style="text-align:justify"><img width="658" height="489" src="../../../assets/images/manual_assets/image024.gif" align="right" hspace="12">The primary goal of the First Follow-Up is to ensure that
                the device is comfortable and that the patient is able to wear it through the
                night. "Tell me about your experience so far." "So, what do you think of that
                contraption?" You should already have prepared the patient for the learning
                curve, hopefully not having sugar-coated the process prior to wearing the
                device. Remember, it takes time to get used to wearing the device. "My job is
                to make it as comfortable as possible and to help find the position that
                protects your airway the best". Any concerns with comfort or pain should be
                addressed now. Remember, patients will not wear their device if it is not
                comfortable. Do not begin to titrate the device forward until you are certain
                the patient can tolerate the advancement. Once the patient reports he can
                comfortably wear the device nightly, then titration should begin. Establish a
                titration schedule. For a TAP 3, this may be one to two half turns every 5
                days without any pain. For an EMA device, this may be going from a 19 Blue
                band to an 18 Blue band after 5 days without any pain. Most patients can
                tolerate a half to one millimeter advancement every week without problems.</p>


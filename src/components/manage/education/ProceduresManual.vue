                the data in the Sleep Studies tab of the summary sheet, making sure you choose
                the appropriate sleep test type in the drop down menu.</p>

            <p class="MsoNormal" style="margin-left:.75in;text-align:justify;text-indent:
-.25in"><span style="font-family:Symbol">•<span style="font:7.0pt &quot;Times New Roman&quot;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
            </span></span>Schedule the patient for an appointment to review the findings of
                the titration sleep test or alternatively call the patients with the results</p>

            <p class="MsoNormal" style="text-align:justify"><i>&nbsp;</i></p>

            <p class="MsoNormal" style="margin-left:1.0in;text-align:justify"><i>Note: The
                AADSM guidelines for successful treatment are that the Apnea-Hypopnea Index
                (AHI)<span style="color:#0070C0"> </span>is reduced by 50% and to less than 10
                (RDI reduced by 50% and to less than 15). <span style="color:#0070C0"></span>It
                is also advisable to attempt to get the time the SPO2 desaturation is less than
                90% reduced to less than 1% of the night.</i></p>

            <p class="MsoNormal" style="margin-left:1.0in;text-align:justify"><i>&nbsp;</i></p>

            <p class="MsoNormal" style="text-align:justify">IF your titration sleep test
                appears NOT to meet the criteria for success as outlined by the AASM (above),
                then you should continue to titrate their dental device further, utilizing more
                titration sleep testing as needed. Eventually, you will reach a point where
                your titration sleep test meets the successful criteria or the patient cannot
                tolerate further advancement; in either case, once this endpoint is reached,
                record your findings in the patient tracker as treatment complete and the
                appropriate letters to the patient's healthcare providers will be generated and
                sent on your behalf. Keep in mind that depending on the patient's level of
                apnea, CPAP compliance and predictability of other treatment options a patient
                may obtain their maximum medical improvement with dental device <i>(highest
                    success they can achieve) </i>even though they don't meet the AADSM criteria
                for success.</p>

            <p class="MsoNormal" style="text-align:justify">Again:</p>

            <p class="MsoNormal" style="text-align:justify">&nbsp;</p>

            <p class="MsoNormal" style="text-align:justify"><i>The sleep doctor or PCP will ultimately
                determine success or failure of our therapy as well as whether or not a separate
                titration sleep test is necessary.</i></p>

            <p class="MsoNormal" style="text-align:justify">&nbsp;</p>

            <p class="MsoNormal" style="text-align:justify">&nbsp;</p>

            <p class="MsoNormal" style="text-align:justify">Sometimes, you will also have a
                patient who is not following through on the treatment plan you designed for him.
                The reasons for this vary, but if you have reached the point where you know the
                patient is not going to continue with treatment, record this in the software
                under the Patient Tracker. A letter will be generated to inform him that his decision
                not to follow through on treatment has resulted in him being officially
                discharged from your sleep treatment program. The letter also refers the
                patient back to his primary care doctor to revisit other treatment options for
                sleep disordered breathing. </p>


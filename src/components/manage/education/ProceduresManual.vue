            <p class="MsoNormal">&nbsp;</p>

            <p class="MsoNormal" style="text-align:justify"><a name="_Toc350778771"><span class="Heading3Char">Goals of subsequent Follow-Up appointments</span></a>:</p>

            <p class="MsoListParagraphCxSpFirst" style="margin-left:1.0in;text-align:justify;
text-indent:-.25in"><span style="font-family:Symbol">•<span style="font:7.0pt &quot;Times New Roman&quot;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
            </span></span>Evaluate integrity of device</p>

            <p class="MsoListParagraphCxSpMiddle" style="margin-left:1.0in;text-align:justify;
text-indent:-.25in"><span style="font-family:Symbol">•<span style="font:7.0pt &quot;Times New Roman&quot;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
            </span></span>Manage side effects</p>

            <p class="MsoListParagraphCxSpMiddle" style="margin-left:1.0in;text-align:justify;
text-indent:-.25in"><span style="font-family:Symbol">•<span style="font:7.0pt &quot;Times New Roman&quot;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
            </span></span>Confirm patient is titrating device correctly</p>

            <p class="MsoListParagraphCxSpMiddle" style="margin-left:1.0in;text-align:justify;
text-indent:-.25in"><span style="font-family:Symbol">•<span style="font:7.0pt &quot;Times New Roman&quot;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
            </span></span>Work to recapture the bite</p>

            <p class="MsoListParagraphCxSpLast" style="margin-left:1.0in;text-align:justify;
text-indent:-.25in"><span style="font-family:Symbol">•<span style="font:7.0pt &quot;Times New Roman&quot;">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
            </span></span>Evaluate subjective tests <i>(Subjective section of Summary
                Sheet)</i> and provide guidance for titration</p>

            <p class="MsoNormal" style="text-align:justify">&nbsp;</p>

            <p class="MsoNormal" style="text-align:justify">This section outlines the ongoing
                process of addressing side effects and continued titration of the patient's
                device. Keep in mind that successful titration starts with normalizing the
                subjective findings first, before moving on to the objective titration sleep
                test. Using your professional judgment, make a decision at each follow-up
                appointment on whether or not you think the patient is treated successfully at
                that particular setting on the dental device. Understand this is subjective;
                it is your impression only, based on improvements in how the patient feels, how
                the patient is sleeping, ESS and TSS scores, energy level, etc. Subjective
                improvements can readily be tracked via the Summary Tab of the software.
                Establish that you have addressed the patient's chief complaint and then
                compare baseline subjective findings to subsequent device check appointments in
                the Subjective section of the Summary Sheet.</p>

            <p class="MsoNormal" style="text-align:justify">&nbsp;</p>

            <p class="MsoNormal" style="text-align:justify">If your impression is that the
                patient is treated successfully, then schedule him for a titration HST. If
                your impression is that the patient could improve, then instruct the patient to
                continue to titrate the dental device forward in 0.5 to 1.0 mm increments as
                tolerated and then schedule the patient for a follow-up in about a month. Re-assess
                the patient at each follow-up appointment and decide on whether or not a titration
                HST is indicated or there is a need for further titration. You may need to do
                more than one titration HST. (See notes below under titration sleep test
                Appointment)</p>

            <p class="MsoNormal" style="text-align:justify">&nbsp;</p>

            <p class="MsoNormal" style="text-align:justify"><i>You should guide the patient
                to the proper titration of his device until the titration sleep test shows
                success or until he can titrate no further. One of these will be your end
                point. EITHER WAY, your treatment is deemed complete and the patient's
                physician will determine success or failure and next steps. The following
                diagram helps to outline this process:</i></p>

